import fs from 'fs-extra';
import path from 'path';

export default class FSUtils {

  static getFiles(dir: string): string[] {
    let files: string[] = [];
    if (fs.statSync(dir).isDirectory()) { //文件夹
      let x: string[] = fs.readdirSync(dir);
      for (let file of x) {
        let xs = this.getFiles(path.join(dir, file));
        files = files.concat(xs);
      }
    } else {
      files.push(dir);
    }
    return files;
  }

}